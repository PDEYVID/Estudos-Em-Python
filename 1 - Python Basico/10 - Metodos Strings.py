
messagem = 'eu adoro comida caseira'
#index.     0123456789

print(messagem.lower()) #menusculo
print(messagem.upper()) #maiusculo
print(messagem.capitalize()) #primeira letra s string maiusculo
print(messagem.find('c')) #procura a posição da letra no index
print(messagem.replace('caseira', 'comida feita em casa')) #troca de letra ou palavras
print(messagem.strip()) #remove espaços