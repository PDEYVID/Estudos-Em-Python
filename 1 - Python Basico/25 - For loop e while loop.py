# For Loop (loop for):
#   - Um "for loop" é usado quando você sabe exatamente quantas vezes deseja iterar sobre um bloco de código.
#   - Ele itera sobre uma sequência (como uma lista, tupla, string, etc.) ou qualquer objeto iterável.
#   - A sintaxe básica do "for loop" em Python é:
#     for item in sequence:
#         # bloco de código a ser repetido
#   - Aqui, "item" é uma variável que assume o valor de cada elemento na sequência em cada iteração.
#   - Os loops "for" são úteis quando você sabe o número de vezes que deseja executar o bloco de código, como percorrer todos os elementos em uma lista.

# While Loop (loop enquanto):
#   - Um "while loop" é usado quando você deseja repetir um bloco de código enquanto uma condição for verdadeira.
#   - Ele continua repetindo até que a condição especificada seja avaliada como falsa.
#   - A sintaxe básica do "while loop" em Python é:
#     while condition:
#         # bloco de código a ser repetido
#   - O bloco de código dentro do "while loop" é executado repetidamente enquanto a condição especificada for verdadeira.
#   - É importante garantir que a condição seja alterada dentro do bloco de código para evitar um loop infinito.
#   - Os loops "while" são úteis quando você não sabe exatamente quantas vezes deseja executar o bloco de código, mas tem uma condição que precisa ser atendida.

